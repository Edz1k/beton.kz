export function getConcreteGradeBySlug(slug?: string | string[]) {
  if (!slug)
    return null

  const normalized = Array.isArray(slug) ? slug[0] : slug
  return concreteGrades.find(item => item.slug === normalized.toLowerCase()) ?? null
}
